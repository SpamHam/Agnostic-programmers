    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            double timer = Double.parseDouble(txtTimer.getText());
            try {
                BE.BESalary s = allSalarys.get(lstSalaryRapport.getSelectedIndex());
                s.setHours(timer);
                BLLPayroll pay = new BLLPayroll();
                pay.update(s);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Du m√• kun intaste nummre i timer.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

